#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t	position;

	position = ft_strlen((char *)needle);
	while (*needle == '\0')
		return ((char *)haystack);
	while (*haystack != '\0' && len -- >= position)
	{
		if (*haystack == *needle && ft_memcmp(haystack, needle, position) == 0)
			return ((char *)haystack);
		haystack++;
	}
	return (0);
}
